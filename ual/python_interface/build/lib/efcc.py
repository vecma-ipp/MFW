	def __len__(self):
		return len(self.array)
	def __iter__(self):
		return self.array.__iter__()

	def __str__(self, depth=0):
		space = depth*'\t'
		ret = space + 'class coilstruct_arraycoil\n'
		for i in range(len(self.array)):
			ret = ret + space + 'coilstruct_arraycoil[%d] = \n' %(i) + self.array[i].__str__(depth+1)
		return ret

